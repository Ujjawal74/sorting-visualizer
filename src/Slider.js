const Slider = ({ title, min, max, def, handler, disabled }) => {
  return (
    <div className="action">
      <span>{title}</span>
      <input
        onChange={handler}
        type="range"
        min={min}
        max={max}
        defaultValue={def}
        disabled={disabled}
      />
    </div>
  );
};

export default Slider;

